package league.ViewBean;

public class CheckReputationInBean {
	String submitName;
	String submitPassword;
	String clientName;
	String clientIp;
	String rules;
	String uploadPackaging;

	public String getSubmitName() {
		return submitName;
	}
	public void setSubmitName(String submitName) {
		this.submitName = submitName;
	}
	public String getSubmitPassword() {
		return submitPassword;
	}
	public void setSubmitPassword(String submitPassword) {
		this.submitPassword = submitPassword;
	}
	public String getClientName() {
		return clientName;
	}
	public void setClientName(String clientName) {
		this.clientName = clientName;
	}
	public String getClientIp() {
		return clientIp;
	}
	public void setClientIp(String clientIp) {
		this.clientIp = clientIp;
	}
	public String getRules() {
		return rules;
	}
	public void setRules(String rules) {
		this.rules = rules;
	}
	public String getUploadPackaging() {
		return uploadPackaging;
	}
	public void setUploadPackaging(String uploadPackaging) {
		this.uploadPackaging = uploadPackaging;
	}

}
